# What is the $RST (Revenue Share Token)?

The Revenue Share Token entitles you to a share of revenue the Shitcoin Society generates. The revenue is distributed in form of dividends paid out in Satoshi (1 Satoshi equals about {convert(1, 'sat', 'eur')}).